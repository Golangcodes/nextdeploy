func TestEmitDispatchTable_WritesFile(t *testing.T) {
	dir := t.TempDir()
	refs := []ModuleRef{
		{RoutePath: "/", Kind: RouteKindPage, CompiledPath: "server/app/page.js"},
	}
	path, err := EmitDispatchTable(refs, nil, dir)
	if err != nil {
		t.Fatalf("EmitDispatchTable: %v", err)
	}
	if exp := filepath.Join(dir, "_nextdeploy", "dispatch.mjs"); path != exp {
		t.Errorf("path: got %s, want %s", path, exp)
	}
	data, err := os.ReadFile(path) // #nosec G304
	if err != nil {
		t.Fatal(err)
	}
	if !strings.Contains(string(data), "export const staticTable") {
		t.Errorf("file content:\n%s", data)
	}
}

func stringSlicesEqual(a, b []string) bool {
	if len(a) != len(b) {
		return false
	}
	for i := range a {
		if a[i] != b[i] {
			return false
		}
	}
	return true
}
